uint16_t tk_crc16_byte(uint16_t crc, uint8_t data) {
  return (crc >> 8) ^ fcstab_16[(crc ^ (data)) & 0xff];
}

uint16_t tk_crc16(uint16_t crc, const uint8_t* data, int data_length) {
  while (data_length) {
    crc = (crc >> 8) ^ fcstab_16[(crc ^ (*data)) & 0xff];
    data++;
    data_length--;
  }

  return crc ^ ~0U;
}

