uint32_t tk_crc32(uint32_t init, const uint8_t* data, int size) {
  size_t a = 0;
  uint32_t crc = init;
  const unsigned char* ptr = data;

  if (ptr != NULL)
    for (a = 0; a < size; a++) {
      crc = (crc >> 8) ^ crc_tab32[(crc ^ (uint32_t)*ptr++) & 0x000000FFul];
    }

  return (crc ^ 0xFFFFFFFFul);

} /* crc_32 */

